function y = dmyPoly(x)
% Function Name: dmyPoly
%
% Description: derivate function with the input value x
%
% Syntax:  y = myPoly(6)
%
% Inputs:
%    x - x input value for the function
%
% Outputs:
%    y - y value of the derivate function in x
%
% Other m-files required: none
%
% $Revision: R2022a$
% $Author: Christian Lange$
% $Date: April 5, 2022$
y = 2*(x-3);
end